#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <sys/wait.h>

// Программе передается аргумент — целое число N > 0.
// Необходимо создать N-1 дополнительных процессов таким образом,
// чтобы у каждого процесса было не более одного дочернего процесса.
// Каждый из процессов должен вывести ровно одно число так,
// чтобы на выходе получилась строка: 1 2 3 4 ... N
// Между числами — ровно один пробел, строка завершается символом '\n'.

int main(int argc, char *argv[]) {
    if (argc != 2) {
        fprintf(stderr, "Usage: %s <N>\n", argv[0]);
        return 1;
    }

    int n = atoi(argv[1]);
    
    if (n <= 0) {
        fprintf(stderr, "N must be positive\n");
        return 1;
    }

    // Создаем цепочку процессов: каждый процесс создает одного потомка
    // и ждет его завершения, чтобы гарантировать порядок вывода
    for (int i = 1; i < n; i++) {
        pid_t pid = fork();
        
        if (pid == -1) {
            perror("fork");
            return 1;
        }
        
        if (pid == 0) {
            // Дочерний процесс: выводим свое число и завершаемся
            // Число равно i+1 (так как первый процесс вывел 1)
            printf("%d", i + 1);
            
            // Если это не последнее число, выводим пробел
            if (i + 1 < n) {
                printf(" ");
            } else {
                // Если это последнее число, выводим перевод строки
                printf("\n");
            }
            
            // Завершаем дочерний процесс
            return 0;
        } else {
            // Родительский процесс: выводим свое число и ждем потомка
            printf("%d", i);
            
            // Если это не последнее число, выводим пробел
            if (i < n) {
                printf(" ");
            }
            
            // Ждем завершения дочернего процесса
            wait(NULL);
            
            // Завершаем родительский процесс после ожидания потомка
            // Но только если это не самый первый процесс
            // Самый первый процесс (i=1) продолжит выполнение и вернет 0 в конце
            if (i > 1) {
                return 0;
            }
        }
    }
    
    // Самый первый процесс завершается здесь
    // (после того как все потомки отработали)
    return 0;
}
